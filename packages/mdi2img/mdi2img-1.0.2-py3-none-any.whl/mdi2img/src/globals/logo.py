"""_summary_
  This file contains a series of ascii art logo's of the app's name.
"""

from typing import Union, List

__logo_all__: dict[str, Union[str, List]] = {}
__logo_ascii_art__: dict[str, str] = {}
__logo_darling__: dict[str, List] = {}


def _add_ascii_art(name: str, string: str) -> None:
    """_summary_
    This is the function in charge of adding an ascii art to the _logo_ascii_art_ dictionnary.

    Args:
      name (str): _description_: The name of the art
      string (str): _description_: The art itself
    """
    if isinstance(string, list):
        if string[0] == '':
            string.pop(0)
        if string[-1] != '':
            string.append("")
    else:
        if string[-1] != "\n":
            string += "\n"
    __logo_ascii_art__[name] = string
    __logo_all__[name] = string


def _add_darling(name: str, string: str) -> None:
    """_summary_
    This is the function in charge of adding an ascii art to the _logo_ascii_art_ dictionnary.

    Args:
        name (str): _description_: The name of the art
        string (str): _description_: The art itself
    """
    if isinstance(string, list):
        if string[0] == '':
            string.pop(0)
        if string[-1] != '':
            string.append("")
    else:
        if string[-1] != "\n":
            string += "\n"
    __logo_darling__[name] = string
    __logo_all__[name] = string


